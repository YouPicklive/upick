// Map user-facing vibe to internal intent/energy for backend
export function vibeToInternalMapping(vibe: YouPickVibe | null): { intent: VibeEnergy | null; energy: string | null } {
  if (!vibe) return { intent: null, energy: null };
  const info = YOUPICK_VIBES.find(v => v.id === vibe);
  if (!info) return { intent: null, energy: null };
  return { intent: info.mappedIntent, energy: info.mappedEnergy === 'low' ? 'chill' : info.mappedEnergy === 'high' ? 'adventure' : 'social' };
}

export interface VibeInput {
  intent: VibeIntent | null;
  energy: VibeEnergy | null;
  filters: VibeFilter[];
  shoppingSubcategory?: ShoppingSubcategory | null;
  selectedVibe?: YouPickVibe | null;
}

export type RandomnessLevel = 'wild' | 'balanced' | 'specific';

export function computeRandomness(vibe: VibeInput): RandomnessLevel {
  if (vibe.intent === 'surprise' || (!vibe.intent && !vibe.energy && vibe.filters.length === 0)) {
    return 'wild';
  }
  const specificity = (vibe.intent ? 1 : 0) + (vibe.energy ? 1 : 0) + Math.min(vibe.filters.length, 3);
  if (specificity >= 3) return 'specific';
  return 'balanced';
}

// Map intent â†’ category for filtering
export function intentToCategories(intent: VibeIntent | null): string[] {
  switch (intent) {
    case 'food': return ['restaurant', 'cafe', 'bakery', 'food-truck', 'brunch', 'lunch', 'dinner', 'desserts'];
    case 'drinks': return ['bar', 'nightlife', 'cocktail-lounge', 'wine-bar', 'dive-bar', 'rooftop-bar'];
    case 'activity': return ['activity', 'park', 'museum', 'bowling', 'arcade', 'escape-room', 'golf', 'mini-golf', 'hiking', 'workshop'];
    case 'shopping': return ['shopping', 'retail', 'boutique', 'thrift', 'market', 'vintage', 'mall'];
    case 'events': return ['event', 'concert', 'festival', 'live-music', 'art-show', 'pop-up'];
    case 'services': return ['wellness', 'spa', 'yoga', 'fitness', 'gym', 'meditation', 'massage'];
    case 'surprise': return [];
    case null: return [];
    default: return [];
  }
}

